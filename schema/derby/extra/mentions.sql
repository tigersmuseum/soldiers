-- Linking soldiers to sources
--

DROP TABLE MENTIONS;
DROP TABLE SOURCES;

CREATE TABLE "SOURCES" (
	"KEY" VARCHAR(32) NOT NULL,
	"LABEL" VARCHAR(128),
	"SOURCE_REF" VARCHAR(128),
	"FILE" VARCHAR(128),
	CONSTRAINT SOURCES_PK PRIMARY KEY ("KEY")

);

CREATE TABLE "MENTIONS" (
	"SID" INTEGER NOT NULL,
	"SOURCE" VARCHAR(32) NOT NULL,
	"ITEM_REF" VARCHAR(128),
	CONSTRAINT MENTIONS_PK PRIMARY KEY ("SID", "SOURCE"),
	CONSTRAINT MENTIONS_FK1 FOREIGN KEY ("SID") REFERENCES PERSON (SID),
	CONSTRAINT MENTIONS_FK2 FOREIGN KEY ("SOURCE") REFERENCES SOURCES ("KEY")
);
